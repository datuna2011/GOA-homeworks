age = int(input("შეიყვანეთ თქვენი ასაკი"))
if age <= 13:
    print("თქვენ ხართ ბავშვი")
elif age <= 18:
    print("თქვენ ხართ სრულწლოვანი")
elif age <= 30:
    print("თქვენ ხართ ზრდასრული")
else:
    print("თქვენ ხართ მოხუცი")

